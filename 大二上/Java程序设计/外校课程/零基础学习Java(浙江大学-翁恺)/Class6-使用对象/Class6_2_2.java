/**
 * Math类型
 */
package Study;

public class Class6_2_2 {
    public static void main(String[] args) {
        System.out.println(Math.abs(-12)); //12 求绝对值
        System.out.println(Math.round(10.345)); //10 四舍五入
        System.out.println(Math.round(10.645)); //11 四舍五入
        System.out.println(Math.random()); //生成一个0-1的随机数
        System.out.println(Math.pow(2,3)); //8 即2的3次方(浮点型)
    }
}
